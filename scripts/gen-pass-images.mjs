// Generates solid brand-coloured PNGs for the Apple Wallet pass (icon + logo),
// dependency-free, and writes them as base64 into src/lib/wallet/images.ts.
import { deflateSync } from "node:zlib";
import { writeFileSync, mkdirSync } from "node:fs";

const crcTable = (() => {
  const t = new Array(256);
  for (let n = 0; n < 256; n++) {
    let c = n;
    for (let k = 0; k < 8; k++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    t[n] = c >>> 0;
  }
  return t;
})();
function crc32(buf) {
  let c = 0xffffffff;
  for (let i = 0; i < buf.length; i++) c = crcTable[(c ^ buf[i]) & 0xff] ^ (c >>> 8);
  return (c ^ 0xffffffff) >>> 0;
}
function chunk(type, data) {
  const len = Buffer.alloc(4);
  len.writeUInt32BE(data.length, 0);
  const typeBuf = Buffer.from(type, "ascii");
  const body = Buffer.concat([typeBuf, data]);
  const crc = Buffer.alloc(4);
  crc.writeUInt32BE(crc32(body), 0);
  return Buffer.concat([len, body, crc]);
}
function solidPng(w, h, [r, g, b]) {
  const sig = Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]);
  const ihdr = Buffer.alloc(13);
  ihdr.writeUInt32BE(w, 0);
  ihdr.writeUInt32BE(h, 4);
  ihdr[8] = 8; // bit depth
  ihdr[9] = 2; // colour type RGB
  const row = Buffer.alloc(1 + w * 3);
  for (let x = 0; x < w; x++) {
    row[1 + x * 3] = r;
    row[1 + x * 3 + 1] = g;
    row[1 + x * 3 + 2] = b;
  }
  const raw = Buffer.concat(Array.from({ length: h }, () => row));
  return Buffer.concat([
    sig,
    chunk("IHDR", ihdr),
    chunk("IDAT", deflateSync(raw)),
    chunk("IEND", Buffer.alloc(0)),
  ]);
}

const purple = [0x48, 0x06, 0x5a]; // brand-purple #48065A
const b64 = (w, h) => solidPng(w, h, purple).toString("base64");

const out = `// Auto-generated by scripts/gen-pass-images.mjs — solid brand-purple pass art.
export const PASS_ICON = "${b64(29, 29)}";
export const PASS_ICON_2X = "${b64(58, 58)}";
export const PASS_LOGO = "${b64(160, 50)}";
export const PASS_LOGO_2X = "${b64(320, 100)}";
`;
mkdirSync("src/lib/wallet", { recursive: true });
writeFileSync("src/lib/wallet/images.ts", out);
console.log("Wrote src/lib/wallet/images.ts");
